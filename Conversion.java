public class Conversion {
    public static void main(String[] args) {

        // type converstion
        // implicit - compiler only does the job
        int d = 5;
        double f = d;
        System.out.println(f);
        

        // type casting
        // explicit - has to be manually done by the programmer

        double m = 7.5;
        int n = (int) m;
        System.out.println(n);


        

    }
    
}
