public class TypeCasting {
    public static void main(String[] args) {
        int n1 = 10;
        double converted = n1;
        System.out.println("The converted int to double is : " + converted);

        double value = 9.8;
        int casted = (int) value;
        System.out.println("The converted double to int is : " + casted);

    }
}